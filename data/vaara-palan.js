// ─── Lookup Utility ───────────────────────────────────────────────────────────

/**
 * Get vaara palan from a JS Date object or day number (0=Sunday … 6=Saturday).
 * Pass a date string like "17/05/1995" or a Date object.
 */
export function getVaaraPalan(
  input
) {
  let dayIndex;

  if (typeof input === "number") {
    dayIndex = input;
  } else if (typeof input === "string") {
    // Supports "DD/MM/YYYY" format from birthParams
    const parts = input.split("/");
    if (parts.length === 3) {
      const [d, m, y] = parts;
      const date = new Date(`${y}-${m}-${d}`);
      dayIndex = date.getDay();
    } else {
      const date = new Date(input);
      dayIndex = date.getDay();
    }
  } else {
    dayIndex = (input).getDay();
  }

  return VAARA_PALAN[dayIndex] ?? null;
}